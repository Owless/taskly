app.put('/api/tasks/:id', async (req, res) => {
  try {
    const { id } = req.params;
    const updates = req.body;
    
    // –í–∞–ª–∏–¥–∞—Ü–∏—è –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–π –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏
    if (updates.title && (updates.title.length === 0 || updates.title.length > 100)) {
      return res.status(400).json({ 
        success: false, 
        error: '–ù–∞–∑–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –æ—Ç 1 –¥–æ 100 —Å–∏–º–≤–æ–ª–æ–≤' 
      });
    }
    
    if (updates.description && updates.description.length > 500) {
      return res.status(400).json({ 
        success: false, 
        error: '–û–ø–∏—Å–∞–Ω–∏–µ –Ω–µ –¥–æ–ª–∂–Ω–æ –ø—Ä–µ–≤—ã—à–∞—Ç—å 500 —Å–∏–º–≤–æ–ª–æ–≤' 
      });
    }
    
    // –ï—Å–ª–∏ –æ—Ç–º–µ—á–∞–µ–º –∫–∞–∫ –≤—ã–ø–æ–ª–Ω–µ–Ω–Ω—É—é, –¥–æ–±–∞–≤–ª—è–µ–º completed_at –≤ UTC
    if (updates.completed && !updates.completed_at) {
      updates.completed_at = new Date().toISOString();
    } else if (updates.completed === false) {
      updates.completed_at = null;
    }
    
    // –û–±—Ä–µ–∑–∞–µ–º –ø—Ä–æ–±–µ–ª—ã
    if (updates.title) {
      updates.title = updates.title.trim();
    }
    if (updates.description) {
      updates.description = updates.description.trim();
    }
    
    // –ü—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ due_date —Å–±—Ä–∞—Å—ã–≤–∞–µ–º —Ñ–ª–∞–≥–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π
    if (updates.due_date !== undefined) {
      updates.notified_upcoming = null;
      updates.notified_overdue = null;
    }
    
    const { data: task, error } = await supabaseAdmin
      .from('tasks')
      .update({ ...updates, updated_at: new Date().toISOString() })
      .eq('id', id)
      .select()
      .single();

    if (error) throw error;

    res.json({ success: true, task });
  } catch (error) {
    console.error('Update task error:', error);
    res.status(400).json({ success: false, error: error.message });
  }
});

